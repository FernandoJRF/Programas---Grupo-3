
# Conteo de las l√≠neas del texto con entrada de usuario y registrando los resultados

# Abrir el archivo de registro donde se almacenar√°n los resultados
with open('registro_conteo_lineas.txt', 'a') as registro_file:
    while True:
        # Solicitar al usuario que ingrese el nombre del archivo
        archivo = input("Por favor, ingresa el nombre del archivo (con su extensi√≥n) o escribe 'salir' para terminar: ")

        # Si el usuario escribe 'salir', se termina el ciclo
        if archivo.lower() == 'salir':
            print("\n‚ú® ¬°Hasta luego! Gracias por usar el programa. ‚ú®")
            break

        # Abrir el archivo y contar las l√≠neas
        try:
            with open(archivo, 'r') as file:
                lines = file.readlines()
                line_count = len(lines)
            
                # Mostrar el resultado 
                print(f"\n‚ú® Conteo de l√≠neas en el archivo '{archivo}' ‚ú®")
                print(f"Total de l√≠neas encontradas: {line_count}\n")

                # Registrar el resultado en el archivo de registro
                registro_file.write(f"Archivo: {archivo} - Total de lineas: {line_count}\n")
        except FileNotFoundError:
            print(f" ‚ö†Ô∏è  El archivo '{archivo}' no se encuentra en la ubicacion especificada.\n")
            registro_file.write(f"Archivo: {archivo} - Error: No se encontro el archivo\n")
        except Exception as e:
            print(f"üî¥ Ocurri√≥ un error: {e}\n")
            registro_file.write(f"Archivo: {archivo} - Error: {e}\n")

